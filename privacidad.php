        <p class="mb-5">El sitio web SocialRoot.io nuestra plataforma SaaS basada en la nube (en adelante la "Plataforma") y los servicios que ponemos a nuestra disposición (en adelante el "Servicio") son proporcionados por SocialRoot., una compañía argentina (en adelante la "Compañía" o "nosotros"). Nos tomamos en serio la protección de su información. Esta Política de privacidad (en adelante la "Política") explica cómo recopilamos y usamos su información, el usuario de nuestro Servicio (en adelante "usted") y los usuarios finales o visitantes de sus sitios web y / o aplicaciones (en adelante "Usuarios finales"). Junto con nuestros Términos de uso, rige su uso del Servicio. Al utilizar el Servicio o proporcionar información, usted acepta los términos de esta Política y las prácticas descritas en este documento. Tenga en cuenta que su información personal y la de sus Usuarios finales pueden procesarse en países donde las leyes sobre el procesamiento de la información personal pueden ser menos estrictas que las leyes de su país.

Fuentes de información que recopilamos
Recopilamos información sobre usted de varias fuentes, incluidas las siguientes:

1. Datos proporcionados a nosotros
Recibimos datos de la computadora, teléfono móvil u otro dispositivo utilizado para acceder al Servicio. Esto puede incluir la dirección IP, el tipo de navegador, el sistema y el idioma, la fecha y la hora de las visitas, el tipo de servicio de Internet, la ubicación (incluida la ubicación GPS cuando esté disponible para nosotros), las páginas visitadas y la forma de interacción con los anuncios y otros tipos de contenido.

2. Información técnica
Recibimos datos de la computadora, teléfono móvil u otro dispositivo utilizado para acceder al Servicio. Esto puede incluir la dirección IP, el tipo de navegador, el sistema y el idioma, la fecha y la hora de las visitas, el tipo de servicio de Internet, la ubicación (incluida la ubicación GPS cuando esté disponible para nosotros), las páginas visitadas y la forma de interacción con los anuncios. y otros tipos de contenido.
- "Cookies": archivos de datos, que a menudo incluyen un identificador único, enviados a un navegador o aplicación de Internet y colocados en una computadora o dispositivo móvil para almacenar cierta información. Podemos utilizar cookies de sesión (que caducan una vez que se cierra el navegador web) y cookies persistentes (que permanecen en el dispositivo hasta que se eliminan). Parte del contenido incluido en el Servicio puede contener y enviar cookies a su computadora o dispositivo móvil. Por lo general, puede configurar su navegador web para rechazar todas las cookies o para indicar cuándo se recibe una cookie; Sin embargo, esto puede interferir con el uso del Servicio. Para obtener más información sobre las cookies, incluido cómo ver qué cookies se han establecido y cómo administrarlas y eliminarlas, consulte la sección de ayuda de su navegador. También utilizamos Google Analytics, Mixpanel, Hot Jar y otros productos similares para obtener datos de uso agregados anónimos. Para optar por no ser rastreado por Google Analytics en todos los sitios web, visite http://tools.google.com/dlpage/gaoptout

- "Balizas" o "Píxeles": archivos electrónicos utilizados para rastrear la navegación a través del Servicio, mensajes de correo electrónico, la finalización de transacciones, la presentación de aplicaciones y el comportamiento de navegación. La exclusión del seguimiento de balizas se puede hacer ajustando la configuración en el navegador web.

- "Archivos de registro": cuando se utiliza el Servicio, podemos rastrear y registrar las acciones que ocurren en el Servicio y recopilar ciertos datos técnicos, incluida la dirección IP, el tipo de navegador, el proveedor de servicios de Internet, las páginas de referencia / salida, el tipo de plataforma, la marca de fecha / hora, los clic en datos y tipos de anuncios vistos.
Tenga en cuenta que usted y sus Usuarios finales no están sujetos a ningún deber legal de proporcionarnos información.

Cómo usamos generalmente la información
Usamos la información que recopilamos, primero, para proporcionarle el Servicio. Recopilamos toda la información disponible para maximizar la relevancia y la calidad del Servicio. Por ejemplo, utilizamos la información que recopilamos para:
-preparar dinámicamente el contenido que ve;
-recordar la información para que no tenga que volver a ingresarla durante su visita o la próxima vez que visite el sitio;
-proporcionar contenido personalizado e información a nuestros usuarios;
-monitorear y mejorar la efectividad del Servicio y nuestro marketing;
-monitorear métricas agregadas e información estadística sobre el Servicio y los usuarios del mismo;
-diagnosticar o corregir problemas tecnológicos reportados por nuestros usuarios o ingenieros;
-ayudarlo a acceder de manera eficiente a su información después de iniciar sesión.

También utilizamos la información que recopilamos con el fin de administrar y expandir el Servicio, proporcionar servicio al cliente y poner a disposición otros sitios y servicios para nuestros usuarios y posibles usuarios.

Comunicación contigo
Ocasionalmente, también podemos utilizar su información de contacto para contactarlo y notificarle sobre cambios importantes en el Servicio, nuevos servicios y oportunidades especiales que creemos que encontrará valiosas, en nuestro nombre y en nombre de nuestros socios y afiliados. Podemos contactarlo por correo electrónico, el Servicio y otros canales similares. Al proporcionarnos su información de contacto, usted acepta que usemos dicha información de contrato para enviarle dichas comunicaciones. Puede notificarnos en cualquier momento si no desea recibir comunicaciones promocionales por correo electrónico enviándonos un correo electrónico a msproof.app@gmail.com o haciendo clic en "cancelar suscripción" en el mensaje.

Información de identificación personal
La información que se nos proporciona puede incluir información de identificación personal ("IIP") que es información que se puede usar para identificar, contactar o ubicar o identificar de otra manera a un individuo, como nombre, dirección, foto de perfil, número de teléfono, información de perfil , dirección de correo electrónico organizacional y / o privada y título.
No alquilamos, vendemos ni difundimos IIP a terceros sin su consentimiento, que no sea:
-divulgación a nuestros empleados, consultores, contratistas, proveedores de servicios y proveedores. Dicha divulgación está normalmente sujeta a la obligación del destinatario de las obligaciones de confidencialidad y la prevención de cualquier derecho independiente de usar esta información por parte de los destinatarios, excepto cuando sea necesario para ayudarnos a proporcionar el Servicio.
-para cumplir con las leyes y regulaciones aplicables o para responder a una citación, orden de allanamiento u otra solicitud legal de información que recibimos, ya sea que la ley aplicable requiera o no una respuesta, o si creemos de buena fe, esa divulgación es necesaria para cumplir con lo anterior;
-si creemos, de buena fe, que dicha divulgación es apropiada o necesaria para hacer cumplir nuestros derechos, cobrar pagos, tomar precauciones contra responsabilidades, investigar y defender contra cualquier reclamo o denuncia, ayudar a las agencias gubernamentales de cumplimiento, proteger la seguridad o integridad de nuestra aplicación y servidores y proteger derechos o propiedad;
-con cualquiera de nuestros padres, o entidad afiliada o sucesora;
-para consumar, y como parte de cualquier venta de activos, transferencia, fusión o liquidación.
Puede elegir voluntariamente si nos proporciona o no su IIP. Puede solicitar acceso a toda su IIP proporcionada voluntariamente que recopilamos en línea y mantenemos en nuestra base de datos enviando una solicitud por escrito a msproof.app@gmail.com.

Uso de información de identificación no personal
En general
Podemos proporcionar a terceros cierta información sobre usted y los Usuarios finales sin identificarlo a usted y a los Usuarios finales como individuos. Hacemos esto para fines tales como (1) proporcionar datos estadísticos y agregados a investigadores y otros consumidores de datos; (2) analizar cómo se usa el Servicio; (3) diagnosticar y resolver problemas técnicos; (4) mantenimiento de la seguridad. Nos reservamos el derecho de usar y divulgar cualquier información anónima a nuestra discreción.

Publicidad
Como se indicó anteriormente, podemos publicar anuncios a través del Servicio. Dichos anuncios pueden basarse en la información que se nos proporciona además de la IIP.

Desactivación y eliminación de su información
SocialRoot elimina su IIP periódicamente, cada 6-12 meses. Si desea eliminar o eliminar cualquier IIP almacenada con nosotros de forma permanente, puede enviar un correo electrónico a msproof.app@gmail.com. Incluya "Eliminar mi información" en la línea de asunto y su nombre completo y dirección de correo electrónico en el cuerpo del correo electrónico. Responderemos a su solicitud de manera oportuna, normalmente dentro de treinta (30) días. Ante dicha solicitud, haremos nuestros mejores esfuerzos para eliminar toda la IIP solicitada de nuestra base de datos. Sin embargo, tenga en cuenta que es posible que parte de la información no se elimine de todas las bases de datos y registros de respaldo. También podemos retener cierta IIP con fines de servicio al cliente, legales y similares. Nos reservamos el derecho de mantener toda la información de identificación personal y continuar usando dicha información de acuerdo con esta Política después de la cancelación de su cuenta y la eliminación de toda la información de identificación personal.

Seguridad de información
Utilizamos medidas estándar de la industria para garantizar la seguridad de toda la información, incluido el uso de contraseñas, cortafuegos, cifrado y servidores remotos, y evaluamos rutinariamente nuestras prácticas para identificar amenazas de seguridad u oportunidades para la mejora de nuestro Servicio. Sin embargo, no se garantiza que la transmisión de información sea completamente segura. La entrada o el uso no autorizados, la falla de hardware o software y otros factores pueden comprometer la seguridad de la información. Usted reconoce y acepta asumir este riesgo cuando se comunica con nosotros.
Privacidad infantil
No recopilamos ni solicitamos información personal de ninguna persona menor de 13 años ni permitimos que dichas personas se registren en el Servicio. Si tiene menos de 13 años, no utilice el Servicio y no nos envíe ninguna información sobre usted. Nadie menor de 13 años puede proporcionarnos información personal. En caso de que sepamos que hemos recopilado información personal de un niño menor de 13 años sin verificación del consentimiento de los padres, borraremos esa información lo más rápido posible. Si cree que podríamos tener información de un niño menor de 13 años o sobre él, comuníquese con nosotros de inmediato a msproof.app@gmail.com.

Cambios a nuestra política de privacidad
Al usar el Servicio, usted acepta que recopilemos y usemos su información personal como se describe en esta Política. Si realizamos cambios sustanciales a esta Política, le notificaremos mediante una publicación sobre dichos cambios en el sitio web del Servicio o por otros medios, según nuestro exclusivo criterio. Nos reservamos el derecho de modificar esta Política en cualquier momento, por lo tanto, revísela con frecuencia. Los cambios a esta Política son vinculantes y efectivos cuando se publican en esta página. En la medida en que el Servicio tenga su dirección de correo electrónico registrada, podemos usarla para informarle sobre cambios en esta Política.

Contactando con nosotros
Si tiene alguna pregunta sobre esta Política, nuestras prácticas de información o sus tratos con nosotros, puede contactarnos en msproof.app@gmail.com.



